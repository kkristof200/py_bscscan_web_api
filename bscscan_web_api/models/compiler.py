# ------------------------------------------------------------ Imports ----------------------------------------------------------- #

# System
from typing import Optional

# Pip
from jsoncodable import JSONCodable

# -------------------------------------------------------------------------------------------------------------------------------- #



# -------------------------------------------------------- class: Compiler ------------------------------------------------------- #

class Compiler(JSONCodable):

    # --------------------------------------------------------- Init --------------------------------------------------------- #

    def __init__(
        self,
        name: str,
        version: str,
        issues_message: Optional[str]
    ):
        self.name = name
        self.version = version
        self.issues_message = issues_message
        self.has_issues = self.issues_message is not None


